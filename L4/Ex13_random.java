import java.util.Scanner;
import java.util.Arrays;
public class Ex12_traverse_2d_array {
  public static void main(String[] args) {
    System.out.println(Math.random()); // return 0 to 1.0
    System.out.println((int)(Math.random()*100)); // int 0 to 100
    System.out.println(60 + (int)(Math.random()*40)); // 60 to 100
  }
}
